export function BottomNav() {
  const pathname = usePathname()
  const [isAdmin, setIsAdmin] = useState(false)

  useEffect(() => {
    const supabase = createClient()

    const checkAdmin = async (userId: string) => {
      const { data } = await supabase
        .from('sb_profiles')
        .select('is_admin')
        .eq('id', userId)
        .single()
      setIsAdmin(data?.is_admin ?? false)
    }

    supabase.auth.getUser().then(({ data }: { data: { user: { id: string } | null } }) => {
      if (data.user) checkAdmin(data.user.id)
    })

    const { data: { subscription } } = supabase.auth.onAuthStateChange((_event: string, session: { user: { id: string } } | null) => {
      if (session?.user) {
        checkAdmin(session.user.id)
      } else {
        setIsAdmin(false)
      }
    })

    return () => subscription.unsubscribe()
  }, [])

  // Don't show nav on auth pages
  if (pathname === '/login' || pathname === '/verify') {
    return null
  }

  const visibleItems = navItems.filter(item => !item.adminOnly || isAdmin)

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-zinc-900/95 backdrop-blur border-t border-zinc-800 pb-safe fixed-ios">
      <div className="flex items-center justify-around h-14">
        {visibleItems.map((item) => {
          const isActive = pathname === item.href ||
            (item.href === '/admin' && pathname.startsWith('/admin'))
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center w-full h-full gap-1 transition-colors ${
                isActive
                  ? 'text-blue-400'
                  : 'text-zinc-500 hover:text-zinc-300'
              }`}
            >
              {item.icon}
              <span className="text-xs">{item.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
